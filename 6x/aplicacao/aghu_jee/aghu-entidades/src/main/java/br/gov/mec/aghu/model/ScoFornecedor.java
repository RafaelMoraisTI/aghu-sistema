	@PrePersist
	@PreUpdate
	@SuppressWarnings("unused")
	private void validarConstraints() {
		if(!((DominioTipoFornecedor.FNA.equals(this.getTipoFornecedor()) && (this.getCgc() == null || this.getCpf() == null))
				||(DominioTipoFornecedor.FNE.equals(this.getTipoFornecedor())))){
			throw new BaseRuntimeException(ScoFornecedorExceptionCode.SCO_FRN_CK3);
		}
		
		if(!(this.getInscricaoEstadual() == null || DominioTipoFornecedor.FNA.equals(this.getTipoFornecedor()))){
			throw new BaseRuntimeException(ScoFornecedorExceptionCode.SCO_FRN_CK6);
		}
		
		if(!(this.getClassificacaoEconomica() == null || DominioTipoFornecedor.FNA.equals(this.getTipoFornecedor()))){
			throw new BaseRuntimeException(ScoFornecedorExceptionCode.SCO_FRN_CK7);
		}
		
		if(!((this.getCrc() != null && this.getDtValidadeCrc() != null) || (this.getCrc() == null) )){
			throw new BaseRuntimeException(ScoFornecedorExceptionCode.SCO_FRN_CK9);
		}
	}
	
