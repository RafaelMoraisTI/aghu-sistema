package br.gov.mec.aghu.exames.vo;


public class ImprimirExamesRealizadosAtendimentosDiversosVO {

	private Integer seq;
	private String grupo;
	private String codConvenio;
	private String descrConvenio;
	private String nome;
	private String projeto;
	private String soeSeq;
	private String nomeExame;
	private String phi;
	private Double valor;
	private Integer qtde;
	
	public String getGrupo() {
		return grupo;
	}
	public void setGrupo(String grupo) {
		this.grupo = grupo;
	}
	public String getCodConvenio() {
		return codConvenio;
	}
	public void setCodConvenio(String codConvenio) {
		this.codConvenio = codConvenio;
	}
	public String getDescrConvenio() {
		return descrConvenio;
	}
	public void setDescrConvenio(String descrConvenio) {
		this.descrConvenio = descrConvenio;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getProjeto() {
		return projeto;
	}
	public void setProjeto(String projeto) {
		this.projeto = projeto;
	}
	public String getSoeSeq() {
		return soeSeq;
	}
	public void setSoeSeq(String soeSeq) {
		this.soeSeq = soeSeq;
	}
	public String getNomeExame() {
		return nomeExame;
	}
	public void setNomeExame(String nomeExame) {
		this.nomeExame = nomeExame;
	}
	public String getPhi() {
		return phi;
	}
	public void setPhi(String phi) {
		this.phi = phi;
	}
	public Double getValor() {
		return valor;
	}
	public void setValor(Double valor) {
		this.valor = valor;
	}
	public Integer getQtde() {
		return qtde;
	}
	public void setQtde(Integer qtde) {
		this.qtde = qtde;
	}
	public Integer getSeq() {
		return seq;
	}
	public void setSeq(Integer seq) {
		this.seq = seq;
	}


}
