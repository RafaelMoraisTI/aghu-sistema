package br.gov.mec.aghu.blococirurgico.vo;


public class RelatorioResumoCirurgiasRealizadasPorPeriodoVO {

	private Integer totalCanceladas;
	private Integer totalPendentesRet;
	private Integer totalEmerg;
	private Integer totalUrg;
	private Integer totalAprovSala;
	private Integer totalEletiva;
	private Integer totalCirurgRealizadas;

	public Integer getTotalCanceladas() {
		return totalCanceladas;
	}

	public void setTotalCanceladas(Integer totalCanceladas) {
		this.totalCanceladas = totalCanceladas;
	}

	public Integer getTotalPendentesRet() {
		return totalPendentesRet;
	}

	public void setTotalPendentesRet(Integer totalPendentesRet) {
		this.totalPendentesRet = totalPendentesRet;
	}

	public Integer getTotalEmerg() {
		return totalEmerg;
	}

	public void setTotalEmerg(Integer totalEmerg) {
		this.totalEmerg = totalEmerg;
	}

	public Integer getTotalUrg() {
		return totalUrg;
	}

	public void setTotalUrg(Integer totalUrg) {
		this.totalUrg = totalUrg;
	}

	public Integer getTotalAprovSala() {
		return totalAprovSala;
	}

	public void setTotalAprovSala(Integer totalAprovSala) {
		this.totalAprovSala = totalAprovSala;
	}

	public Integer getTotalEletiva() {
		return totalEletiva;
	}

	public void setTotalEletiva(Integer totalEletiva) {
		this.totalEletiva = totalEletiva;
	}

	public Integer getTotalCirurgRealizadas() {
		return totalCirurgRealizadas;
	}

	public void setTotalCirurgRealizadas(Integer totalCirurgRealizadas) {
		this.totalCirurgRealizadas = totalCirurgRealizadas;
	}

}
