package br.gov.mec.aghu.paciente.vo;

public class SumarioAtdRecemNascidoSlPartoGestacoesAnterioresVO {

	private Short ano;
	private String classificacao;
	private String peso;
	private String situacao;
	private String complicacoes;
	private String motivo;
	private String inaDescricao;
	
	public Short getAno() {
		return ano;
	}
	public void setAno(Short ano) {
		this.ano = ano;
	}
	public String getClassificacao() {
		return classificacao;
	}
	public void setClassificacao(String classificacao) {
		this.classificacao = classificacao;
	}
	public String getPeso() {
		return peso;
	}
	public void setPeso(String peso) {
		this.peso = peso;
	}
	public String getSituacao() {
		return situacao;
	}
	public void setSituacao(String situacao) {
		this.situacao = situacao;
	}
	public String getComplicacoes() {
		return complicacoes;
	}
	public void setComplicacoes(String complicacoes) {
		this.complicacoes = complicacoes;
	}
	public String getMotivo() {
		return motivo;
	}
	public void setMotivo(String motivo) {
		this.motivo = motivo;
	}
	public String getInaDescricao() {
		return inaDescricao;
	}
	public void setInaDescricao(String inaDescricao) {
		this.inaDescricao = inaDescricao;
	}
}
