package br.gov.mec.aghu.paciente.prontuario.vo;

public class RelatorioSolicitacaoProntuarioVO {
	private String solicitante;
	private String responsavel;
	private String nomeEspecialidade;
	private String observacao;
	private String secao;
	private String indicador;
	private String prontuarioSemDigito;
	private String campo;
	private Integer prontuario;
	/**
	 * @return the solicitante
	 */
	public String getSolicitante() {
		return solicitante;
	}
	/**
	 * @param solicitante the solicitante to set
	 */
	public void setSolicitante(String solicitante) {
		this.solicitante = solicitante;
	}
	/**
	 * @return the responsavel
	 */
	public String getResponsavel() {
		return responsavel;
	}
	/**
	 * @param responsavel the responsavel to set
	 */
	public void setResponsavel(String responsavel) {
		this.responsavel = responsavel;
	}
	/**
	 * @return the nomeEspecialidade
	 */
	public String getNomeEspecialidade() {
		return nomeEspecialidade;
	}
	/**
	 * @param nomeEspecialidade the nomeEspecialidade to set
	 */
	public void setNomeEspecialidade(String nomeEspecialidade) {
		this.nomeEspecialidade = nomeEspecialidade;
	}
	/**
	 * @return the observacao
	 */
	public String getObservacao() {
		return observacao;
	}
	/**
	 * @param observacao the observacao to set
	 */
	public void setObservacao(String observacao) {
		this.observacao = observacao;
	}
	/**
	 * @return the secao
	 */
	public String getSecao() {
		return secao;
	}
	/**
	 * @param secao the secao to set
	 */
	public void setSecao(String secao) {
		this.secao = secao;
	}
	/**
	 * @return the indicador
	 */
	public String getIndicador() {
		return indicador;
	}
	/**
	 * @param indicador the indicador to set
	 */
	public void setIndicador(String indicador) {
		this.indicador = indicador;
	}
	/**
	 * @return the prontuarioSemDigito
	 */
	public String getProntuarioSemDigito() {
		return prontuarioSemDigito;
	}
	/**
	 * @param prontuarioSemDigito the prontuarioSemDigito to set
	 */
	public void setProntuarioSemDigito(String prontuarioSemDigito) {
		this.prontuarioSemDigito = prontuarioSemDigito;
	}
	/**
	 * @return the campo
	 */
	public String getCampo() {
		return campo;
	}
	/**
	 * @param campo the campo to set
	 */
	public void setCampo(String campo) {
		this.campo = campo;
	}
	/**
	 * @return the prontuario
	 */
	public Integer getProntuario() {
		return prontuario;
	}
	/**
	 * @param prontuario the prontuario to set
	 */
	public void setProntuario(Integer prontuario) {
		this.prontuario = prontuario;
	}
	

}
