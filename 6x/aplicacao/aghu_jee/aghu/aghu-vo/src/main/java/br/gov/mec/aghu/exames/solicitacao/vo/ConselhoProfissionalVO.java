package br.gov.mec.aghu.exames.solicitacao.vo;


public class ConselhoProfissionalVO {

	private String regConselhoMedico;
	private String siglaConselhoMedico;
	private String ufConselhoMedico;

	public String getRegConselhoMedico() {
		return regConselhoMedico;
	}

	public void setRegConselhoMedico(String regConselhoMedico) {
		this.regConselhoMedico = regConselhoMedico;
	}

	public String getSiglaConselhoMedico() {
		return siglaConselhoMedico;
	}

	public void setSiglaConselhoMedico(String siglaConselhoMedico) {
		this.siglaConselhoMedico = siglaConselhoMedico;
	}

	public String getUfConselhoMedico() {
		return ufConselhoMedico;
	}

	public void setUfConselhoMedico(String ufConselhoMedico) {
		this.ufConselhoMedico = ufConselhoMedico;
	}

}
